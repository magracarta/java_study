package variable;

public class Var7 {
    public static void main(String[] args) {
        /* 변수는 데이터를 다루는 종류에 다라 다양한 형식이 존재 타입이라고하고 우리말로는 형식또는 형이라고 한다. */
        /* 리터럴 : 코드에서 직접 적은 값들을 리터럴이라고한다. 문자 글자 등을 의미한다. (고정된 값) */
        
        int a = 100; //정수
        double b = 10.5;//실수
        boolean c = true; //불리언(boolean) true, false 입력가능 참과 거짓을 판단
        char d = 'A'; //문자하나
        String e ="Hello World"; //문자열 문자열을 다루기 위한 특별한 타입


        //int z = "백원"; //오류

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
